    private void deleteDatafromCSV() throws FileNotFoundException, IOException, CsvException{
        
        String csvFile = "mphdetails.csv";
        String empid = text_idsearch.getText();
        String emplname = text_lnamesearch.getText();
        
        if (empid.isEmpty() && emplname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please input Employee Numbers or Employee Last Names.");
            return;
        }
        
        Set<String> empidSet = new HashSet<>(Arrays.asList(empid.split(",")));
        Set<String> emplnameSet = new HashSet<>(Arrays.asList(emplname.split(",")));
        
        
        File inputFile = new File(csvFile);
        File tempFile = new File("temp.csv");
        
        try (CSVReader reader = new CSVReader(new FileReader(inputFile));
             CSVWriter writer = new CSVWriter(new FileWriter(tempFile))) {
            
            List<String[]> allRows = reader.readAll();
            boolean deleted = false;
            
            for (String[] row : allRows) {
                
                if (!(empidSet.contains(row[0]) || emplnameSet.contains(row[1]))) {
                    writer.writeNext(row);
                    
                } else {
                    
                    deleted = true;
                    
                }
                
            }
            
            if (deleted){
                
                JOptionPane.showMessageDialog(this, "Record Deleted Successfully.");
                
            } else {
                
                JOptionPane.showMessageDialog(this, "No Record Found.");
                
            } 
            
        } catch (IOException | CsvException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error deleting data.");

        }if (!inputFile.delete()) {
            System.out.println("Could not delete file");
            JOptionPane.showMessageDialog(this, "Could not delete original file.");
        }
        
        if (!tempFile.renameTo(inputFile)) {
            System.out.println("Could not rename file");
            JOptionPane.showMessageDialog(this, "Could not rename temp file.");
        }
        
    }
    

