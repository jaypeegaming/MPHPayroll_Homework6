    private void searchData() throws FileNotFoundException, IOException, CsvException{
        
        String csvFile = "mphdetails.csv";
        String empid = text_idsearch.getText();
        String emplname = text_lnamesearch.getText();
        
        
        if (empid.isEmpty() && emplname.isEmpty()) {
            
            JOptionPane.showMessageDialog(this,"Please Input Employee Number or Employee Last Name.");
            return;
            
        }
        
        boolean found = false;
        
        try (CSVReader reader = new CSVReader(new FileReader(csvFile))) {
            List<String[]> allRows = reader.readAll();
            
            for (String [] row : allRows) {
                if (row[0].equals(empid) || row[1].equalsIgnoreCase(emplname)) {
                    text_id.setText(row[0]);
                    text_lname.setText(row[1]);
                    text_fname.setText(row[2]);
                    text_bdate.setText(row[3]);
                    text_address.setText(row[4]);
                    text_pnumber.setText(row[5]);
                    text_sss.setText(row[6]);
                    text_philhealth.setText(row[7]);
                    text_tin.setText(row[8]);
                    text_pagibig.setText(row[9]);
                    text_status.setText(row[10]);
                    text_position.setText(row[11]);
                    text_superior.setText(row[12]);
                    text_salary.setText(row[13]);
                    text_rice.setText(row[14]);
                    text_phone.setText(row[15]);
                    text_clothing.setText(row[16]);
                    found = true;
                    break;
                
                } 
            }
            
        } 
            catch (IOException | CsvException en) {
            en.printStackTrace();
            JOptionPane.showMessageDialog(this,"Error Loading of Data");
            
        }
       
    }
    
 
